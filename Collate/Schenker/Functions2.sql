--VERIFICA SE JAH EXISTE A OCOR FINALIZADORA
    SELECT @VAR_EXISTE_ENTREGA = COUNT(*)
    FROM DOCTO_TRANSPORTE DT
    WHERE DT.DOCTO_TRANSPORTE_ID =   @IN_DOCTO_TRANSPORTE_ID
     AND DT.DATA_CHEGADA_CLIENTE_EFETIVA IS NOT NULL;

