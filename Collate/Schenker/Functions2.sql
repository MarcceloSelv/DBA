				AND NF.TAB_STATUS_ID = 1

	)
	,
	AUX2 AS
	(
		  SELECT 
				 nota_fiscal
				,ROW
				,ROW_PAI = CASE ROW WHEN 1 THEN NULL ELSE ROW - 1 END
		  FROM AUX
	)
	,CONCATENAR AS
	(
		  SELECT
				nota_fiscal
				,ROW
		  FROM AUX2 
		  WHERE ROW_PAI IS NULL

		  UNION ALL

		  SELECT
				nota_fiscal = A.nota_fiscal + ' / ' + B.nota_fiscal
				,A.ROW
		  FROM AUX2 A 
		  INNER JOIN CONCATENAR B 
				ON A.ROW_PAI = B.ROW
	)
	SELECT TOP 1 @RETORNO = nota_fiscal
	FROM CONCATENAR
	ORDER BY ROW DESC
	
	RETURN @RETORNO

