DECLARE @VAR_TRACKING_ID_NF    INT = NULL;
DECLARE @VAR_OBSERVACAO_NF     VARCHAR(255) = NULL;
DECLARE @VAR_INDIC_ENTREGA_NF  CHAR(1) = NULL; 

