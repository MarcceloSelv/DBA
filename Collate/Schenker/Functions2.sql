  ELSE IF @IN_TIPO_RESULTADO = 4
  RETURN CONVERT(VARCHAR, @VAR_NUM_COD_OCOR);

  -- 5 - RETORNA CODIGO DA OCORRENCIA / DESCRIÇÃO / DATA
  ELSE IF @IN_TIPO_RESULTADO = 5 
