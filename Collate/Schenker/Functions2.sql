	AND LIGA_REGIAO.TAB_STATUS_ID = 1
	AND LIGA_REGIAO_ITEM.TAB_STATUS_ID = 1
	AND REGIAO.TAB_STATUS_ID = 1
	AND REGIAO_MUNIC.TAB_STATUS_ID = 1
	AND MUNICIPIO.TAB_STATUS_ID = 1	

