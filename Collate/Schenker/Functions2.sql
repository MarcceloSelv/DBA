		        SELECT @Val_aux_char = SUBSTRING(@Val_aux_char, 1, @Posicao + @Qtde_Casas)
		  END
		Else
		  BEGIN
        		SELECT @aux = ''
			select @i = @Dif
			WHILE @i <= @Qtde_Casas - 1
