				SELECT 1
				FROM VREGIOES_CLI_FOR VR
				WHERE (
						VR.PESSOA_ID = @IN_CLIENTE_ID
						OR @IN_CLIENTE_ID IS NULL
						OR VR.GRUPO_CLIENTE_ID = (SELECT CL.GRUPO_CLIENTE_ID FROM CLIENTE CL WHERE CL.PESSOA_ID = @IN_CLIENTE_ID)
