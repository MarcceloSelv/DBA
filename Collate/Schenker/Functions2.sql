	  end


	select @Valor = 1 / power(10 , @Qtde_Casas)
	If @Param < @Valor And @Param > (-1 * @Valor)
	  begin
    		select @Valor = 1 / power(10 , (@Qtde_Casas + 1))
 		If @Param < @Valor
