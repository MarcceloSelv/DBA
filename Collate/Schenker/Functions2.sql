DECLARE @VAR_TRACKING_ID       INT = NULL;
DECLARE @VAR_OBSERVACAO        VARCHAR(255) = NULL;  
DECLARE @VAR_INDIC_ENTREGA     CHAR(1) = NULL; 
  
