	    End

	Close Cursor_NF_Ocorrencia_Entregue
	Deallocate Cursor_NF_Ocorrencia_Entregue

	Set @Lista_Notas = SubString(@Lista_Notas, 1, Len(@Lista_Notas) - 1)

