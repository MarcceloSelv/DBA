		SELECT RG.NOME, RG.REGIAO_ID
		FROM REGIAO RG 
		INNER JOIN REGIAO_MUNIC RM
			ON RG.REGIAO_ID = RM.REGIAO_ID
		WHERE 
				RG.TAB_STATUS_ID = 1
			AND RM.TAB_STATUS_ID = 1
			AND RM.MUNICIPIO_ID IS NULL
